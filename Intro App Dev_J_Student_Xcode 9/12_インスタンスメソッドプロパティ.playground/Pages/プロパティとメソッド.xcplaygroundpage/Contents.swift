 ### どちらも頻繁に使用する
 
 アプリケーションを構築する時、記述するほぼすべてのコードは型のインスタンスメソッドまたはプロパティの形式です。また、作成する型についてのコードもあります。アプリケーションは、様々な型の多くのインスタンスが連係してできています。
 
