 - callout(演習): 
 上の手順に従ってマニュアルビューアを開き、ウインドウの上部にある検索バーを使って`String`を検索してください。`String`型に関する説明を確認します。
 
 検索結果はこのように表示されます。最初の項目を選択すると、`String` APIの説明が表示されます。
 
 ![Stringの検索結果](StringSearch.png)
 
 >(注意):
マニュアルは「あらゆること」をカバーしているので、非常に複雑に見えることがあります。Wikipediaの情報をすべて読み、理解し、覚える必要がないように、マニュアルの情報をすべて読み、理解し、覚える必要はありません。必要な時に必要な情報を得るために使ってください。
 
