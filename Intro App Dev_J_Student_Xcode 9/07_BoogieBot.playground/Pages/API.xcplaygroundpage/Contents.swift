 ## API
 
 アプリケーション開発者は常に、ほかの人から提供されるコードを扱います。BoogieBotでは、BoogieBotを開始し、動かし、色を変更するいくつかの関数が定義されています。
 
 BoogieBotをコントロールする関数のセットは、「アプリケーションプログラミングインターフェイス」（API）の一例です。
 
 APIは、ソフトウェア開発者がタスクを完了するために使うことのできる、特定の関数のセットです。この場合、BoogieBot APIはソフトウェアのロボットダンスを作っています。
 
  ### BoogieBot API
 BoogieBotを開始する：
 - `startBot()`
 
 BoogieBotを動かす：
 
 - `leftArmUp()`、`leftArmDown()`、`rightArmUp()`、`rightArmDown()`
 - `leftLegUp()`、`leftLegDown()`、`rightLegUp()`、`rightLegDown()`
 - `shakeItLeft()`、`shakeItRight()`、`shakeItCenter()`
 - `jumpUp()`、`jumpDown()`
 
 BoogieBotの色を変える：
 - `fabulize()`、`defabulize()`
 
 次は、これらの関数を組み立ててダンスのルーチンを作る方法を学びます。
 
