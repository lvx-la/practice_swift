 ## まとめ
 
アプリケーションには、使用するデータを表すモデルが必ずあります。
 
Swiftでは独自の型を定義してアプリケーションに適した方法でデータを整理できます。
 
カスタム型を作成するには、構造体を定義する方法があります。

構造体は、プロパティとメソッドをまとめてグループ化できる型です。
 
一部のプロパティは、変数や定数のようにデータを保持します。計算された値を返すものもあります。

カスタム型のインスタンスを作成および使用する方法は、Swiftのほかの型と同じです。
 
カスタム型は、関数のパラメータや戻り値の型として使うなど、ほかの任意の型と同じように使います。

