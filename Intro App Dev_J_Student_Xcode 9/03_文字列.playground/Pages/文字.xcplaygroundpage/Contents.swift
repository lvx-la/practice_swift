//: ## 文字
//: テキストに含まれるのはアルファベットだけではありません。句読点、数字、記号、空白なども含まれます。これらはすべて文字と呼ばれ、文字列は文字から成ります。
//:
//: アルファベットは文字です。
//: ## Aa Bb Cc Dd Ee
//: 句読点も文字です。
//: ## ! ? . , ; :
//: 数字：
//: ## 1 2 3 4 5 6 7 8 9 0
//: 記号：
//: ## # @ $ & © π * + - /
//: 空白やタブなど目に見えない文字もあります。\
//: （見えませんが、以下にあります。）
//: ## `                         `
