 船（または宇宙船）の船長は航海日誌（ログ）に航海に関するあらゆる情報を記録すると聞いたことがあるかもしれません。アプリケーションにもログがあるのを知っていましたか？
 
 プログラマがコンソールにメッセージを出力するのは、通常、プログラムの実行に関する情報を記録、すなわち「ログ」するためです。コンソールにメッセージを出力することを「ロギング」と言います。これらのメッセージは「ログメッセージ」と呼ばれることがあります。
 
 プログラマは多くの場合、ログメッセージを使って、何かがうまくいかなかったことや何か予期しないことが発生したことを示します。以下のように、メッセージによって警告したり、問題の診断を助けたりします。
 
