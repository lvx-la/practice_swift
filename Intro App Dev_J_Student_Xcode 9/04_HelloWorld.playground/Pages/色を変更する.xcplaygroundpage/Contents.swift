 ## 色を変更する

プログラマの多くは、快適なコンピューティング環境を整えたいと考えます。Xcodeでは、メニューバーの「Xcode」>「Preferences」を選択して環境を変更できます。

 ![「Xcode」メニューのメニューバーが開きます。 ](xcode-preferences.png)

「Preferences」ウインドウで「Fonts &amp; Colors」タブを選択し、「Source Editor」が選択されていることを確認します。

 ![Xcode環境設定パネルの「Fonts &amp; Colors」タブ](xcode-pref-fonts-and-colors.png)


レトロスタイルにするなら、濃い背景の「Civic」「Dust」「Midnight」のいずれかを選んでください。

 ![Xcode環境設定パネルの「Fonts &amp; Colors」タブで「Dusk」オプションを選択したところ](xcode-pref-dusk-color.png)


