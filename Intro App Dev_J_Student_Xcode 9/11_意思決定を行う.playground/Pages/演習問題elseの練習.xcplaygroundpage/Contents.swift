/*:
 ## 演習問題：elseの練習
 
 先ほど見たように、`else`文は`if`文の結果がtrueでない時に実行するコードを持っています。elseを使うことで必要なコードの量が減り、プログラムの流れが明確になります。
*/
let secretWord = "banana"
let guess = "apple"

if secretWord == guess {
    print("Your guess is right.")
}
if secretWord != guess {
    print("Your guess is wrong.")
}
