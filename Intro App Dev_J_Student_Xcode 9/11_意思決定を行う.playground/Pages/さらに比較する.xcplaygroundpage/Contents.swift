1 > 2
2 > 2
3 > 2
/*:
 2つの記号を並べて1つの意味を表す比較もあります。
 */
// 等しくない
1 != 2
2 != 2
2 != 1

// 以下
2 <= 2
1 <= 2
3 <= 2

// 以上
1 >= 2
2 >= 2
3 >= 2

