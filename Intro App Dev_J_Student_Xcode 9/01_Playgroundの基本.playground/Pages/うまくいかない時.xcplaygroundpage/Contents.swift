//: プログラマは、ミスをすることがあります。\
//: （皆さんが考えているよりも頻繁にミスをします）
//:
//: あなたも、コードを記述してアプリケーションを構築する時にミスをするでしょう。
//:
//: あわてる必要はありません。
//:
//: 標準的な当たり前のコードを記述していても、うまくいかない時があります。\
