//:
//: うまくいかない時のトラブルシューティングは、プログラマの大切な仕事です。
//:
//: まずは、落ち着きましょう。深呼吸をしましょう。
//:
//: あわてる必要はありません。
//:
//: エラーになっても、失敗したとか、プログラマとして未熟だというわけではありません。\
