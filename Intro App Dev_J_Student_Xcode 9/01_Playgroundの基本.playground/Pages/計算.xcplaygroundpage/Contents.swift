//: ## 計算
//: Playgroundを計算機のように使うことができます。計算式を入力すると、答え、すなわち計算の「結果」が結果サイドバーに表示されます。
//:
//: 足し算と引き算には、プラス記号「`+`」とマイナス記号「`-`」を使います。
34 + 56 + 230

1000 - 300

//: 掛け算にはアスタリスク「`*`」を使います。
3 * 100
//: 割り算にはスラッシュ「`/`」を使います。
24 / 8
//: グループ化するには、カッコ「`( )`」を使います。
(300 + 500 + 400) / 3
