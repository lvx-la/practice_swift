 ## まとめ
 
 Swiftで使うすべての定数や変数には型があります。
 
 変数の場合、値は変わっても型は変わりません。
 
 型は、以下を表現します。
 - 値が何であるか
 - 値ができること
 - 値を使える場所
 
 コード行を記述する際には、型を不適切に使わないようにチェックが行われます。
 
 Swiftには、代入される値によって定数や変数の型を推測する機能があります。
 
 定数や変数の型を明示的に指定するには、型アノテーションを使います。
 
