//: ## 問題を解決する
//: プログラマは、あらゆる問題を解決するために、利用可能なツールなら何でも使います。
//:
//: このコースを進めながら、ものごとをやり遂げる方法、コードを使って問題を解決する方法、素晴らしいアプリケーションを構築する方法を学んでいきます。最初は基本を使った小規模でシンプルなものですが、スキルや知識が増えると、知っていることを使ってより多くのことができるようになります。
//:
//: プログラマは、自分自身のために問題を解決したりアプリケーションを記述することもあるかもしれませんが、多くの場合はほかの人の問題解決をサポートします。コードの記述方法を学ぶだけでなく、様々な種類の問題について考え、その解決方法を考えることが重要です。
//:
//: このコースを通じて、いくつかの問題解決に取り組んでいただきます。その中で、優れたプログラマに必要な基本事項を学んでいきます。
