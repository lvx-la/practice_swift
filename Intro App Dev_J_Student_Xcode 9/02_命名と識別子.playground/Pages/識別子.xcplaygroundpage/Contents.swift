//: ## 識別子
//: Swiftでは、名前のことを正式には_「識別子」と呼びます。識別子（identifier）という語を最もよく目にするのは、エラーメッセージ内です。プログラマは通常、普段の言葉で「名前」と呼んでいます。_皆さんに慣れていただくため、このコースでは両方の語を使います。
//:
//: 定数の名前は小文字から始まります。下の例のように、複数の単語を含める時は（スペースを入れずに）全部続けてください。間にスペースが入っていると、Swiftでは各単語が別の識別子だと判断されるからです。
//:
//:複数の単語を含む識別子を読みやすくするため、2つ目以降の単語は大文字で始めます。名前の中の大文字がラクダのこぶのように見えることから、このような名前の記述方法を「キャメルケース」と呼びます。 🐫
let numberOfDogs = 6 + 2

let numberOfCats = 5 - 1

let numberOfTurtles = 2 + 1

let numberOfHamsters = 1 + 1

let totalNumberOfAnimals = numberOfDogs + numberOfCats + numberOfTurtles + numberOfHamsters

let totalNumberOfMammals = numberOfDogs + numberOfCats + numberOfHamsters

