 ## まとめ
 
 このPlaygroundでは、非常に重要な概念を学習しました。
 
 - `let`で宣言される値は定数で、いったん値が代入されると変更できません。このような値を不変と呼びます。
 - `var`で宣言される値は変数で、時間と共に新しい値を代入できます。このような値を可変と呼びます。
 - 可変値は、`score = score + 10`のように、それ自身に値を代入するステートメントの一部として使用できます。
 - `score += 10`のように、複合代入演算子を使って可変値をアップデートできます。
 - 定数と変数を適切な場所で使うと、コードがより安全で理解しやすくなります。
 
