/*:
 ## 変数の宣言
 
 変数の宣言は、定数の宣言とほとんど同じです。ただし、キーワード`let`の代わりにキーワード`var`を使います。
 */
// 定数を宣言する
let placeOfBirth = "New Jersey"

// 変数を宣言する
var currentLocation = "New Jersey"

/*:
 変数を宣言した後に、新しい値を代入できます。
*/
// 変数に新しい値を代入する
currentLocation = "California"

